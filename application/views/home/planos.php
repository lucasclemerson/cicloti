<script>
    $(document).ready(function() {
        let divs = $('.item-carousel');
        let currentIndex = 0;
        let interval;
    
        function showNextDiv() {
            $(divs[currentIndex]).fadeOut(1000, function() {
                currentIndex = (currentIndex + 1) % divs.length;
                $(divs[currentIndex]).fadeIn(1000);
            });
        }
    
        function startFadeEffect() {
            interval = setInterval(showNextDiv, 10000); // Change divs every 5 seconds
        }
    
        function stopFadeEffect() {
            clearInterval(interval);
            dontHidden();
        }
    
        function checkMediaQuery() {
            if (window.matchMedia('(min-width: 1100px)').matches) {
                console.error("stop carousel.")
                stopFadeEffect();
            } else {
                console.warn("start carousel.")
                for (let i=1; i < divs.length;i++){
                    divs[i].classList.add('hidden');
                }
                startFadeEffect();
            }
        }
        
        function dontHidden (){
            for (let i=0; i < divs.length;i++){
                if (divs[i].classList.contains('hidden')){
                    divs[i].classList.remove('hidden');
                }
            }            
        }
    
        // Initial check
        checkMediaQuery();
    
        // Add event listener
        window.addEventListener('resize', checkMediaQuery);
    });
</script>
